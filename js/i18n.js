function applyLanguage(lang) {
    localStorage.setItem('appLang', lang);
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    const titleElements = document.querySelectorAll('[data-i18n-title]');
    titleElements.forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (translations[lang] && translations[lang][key]) {
            el.setAttribute('title', translations[lang][key]);
            // Re-initialize tooltip if BS is loaded
            if (typeof bootstrap !== 'undefined' && bootstrap.Tooltip) {
                const tooltipInstance = bootstrap.Tooltip.getInstance(el);
                if (tooltipInstance) {
                    tooltipInstance.dispose();
                }
                new bootstrap.Tooltip(el);
            }
        }
    });

    // Update placeholders dynamically if needed
    const customTitleInput = document.getElementById('customPageTitleInput');
    if (customTitleInput && translations[lang] && translations[lang]['title_placeholder']) {
        customTitleInput.placeholder = translations[lang]['title_placeholder'];
    }
    
    // Handle RTL
    if (lang === 'ar') {
        document.documentElement.setAttribute('dir', 'rtl');
        document.documentElement.setAttribute('lang', 'ar');
    } else {
        document.documentElement.setAttribute('dir', 'ltr');
        document.documentElement.setAttribute('lang', lang);
    }
}

// Initial language setup
window.addEventListener('load', () => {
    const savedLang = localStorage.getItem('appLang') || 'en';
    
    // Auto-select language dropdown if it exists
    const langSelect = document.getElementById('langSelect');
    if (langSelect) {
        langSelect.value = savedLang;
        langSelect.addEventListener('change', (e) => applyLanguage(e.target.value));
    }
    
    applyLanguage(savedLang);
});
